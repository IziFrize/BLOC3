    END IF;
  END
  \$\$;

  DO \$\$
  BEGIN
    IF NOT EXISTS (SELECT FROM pg_roles WHERE rolname = '$WRITER_USER') THEN
      CREATE ROLE $WRITER_USER LOGIN PASSWORD '$WRITER_PASSWORD';
    END IF;
  END
  \$\$;

  GRANT CONNECT ON DATABASE $POSTGRES_DB TO $APP_USER, $ANALYST_USER, $WRITER_USER;
